SELECT
    AVG(SALARY)
FROM
    HR.EMPLOYEES
GROUP BY
    DEPARTMENT_ID;

SELECT
    DEPARTMENT_ID,
    AVG(SALARY),
    MAX(SALARY),
    MIN(SALARY)
FROM
    HR.EMPLOYEES
WHERE
    DEPARTMENT_ID IN (10, 20, 30)
GROUP BY
    DEPARTMENT_ID
HAVING
    AVG(SALARY) <= 5000;

SELECT
    DEPARTMENT_ID,
    SALARY
FROM
    HR.EMPLOYEES
WHERE
    DEPARTMENT_ID = 100;

SELECT
    DEPARTMENT_ID,
    MAX(SALARY)
FROM
    HR.EMPLOYEES
GROUP BY
    DEPARTMENT_ID
HAVING
    MAX(SALARY) > 7500
ORDER BY
    DEPARTMENT_ID;